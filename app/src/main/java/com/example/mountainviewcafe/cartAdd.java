package com.example.mountainviewcafe;

public class cartAdd {
    public String productId, userID, quantity, title, discount, image, description, price, cartID ;

    public cartAdd () {

    }


    public cartAdd(String productId, String userID, String quantity, String title, String discount, String image, String description, String price) {
        this.productId = productId;
        this.userID = userID;
        this.quantity = quantity;
        this.title = title;
        this.discount = discount;
        this.image = image;
        this.description = description;
        this.price = price;
    }

    public String getCartID() {
        return cartID;
    }

    public void setCartID(String cartID) {
        this.cartID = cartID;
    }

    public String getProductId() {
        return productId;
    }

    public void setProductId(String productId) {
        this.productId = productId;
    }

    public String getUserID() {
        return userID;
    }

    public void setUserID(String userID) {
        this.userID = userID;
    }

    public String getQuantity() {
        return quantity;
    }

    public void setQuantity(String quantity) {
        this.quantity = quantity;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getDiscount() {
        return discount;
    }

    public void setDiscount(String discount) {
        this.discount = discount;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }
}
